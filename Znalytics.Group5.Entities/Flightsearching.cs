public class FlightSearching
{
    private int _NoOfFlights;
    private string _FlightName;
    private int _FlightDate;
    private int _Time;
    public void SetNoOfFlight(int value)
    {
        _NoOfFlights = value;
    }
    public int GetNoOfFlight()
    {
        return _NoOfFlights;
    }
    public void SetFlightName(string value)
    {
        _FlightName = value;
    }
    public string GetFlightName()
    {
        return _FlightName;
    }
    public void SetFlightDate(int value)
    {
        _FlightDate = value;

    }
    public int GetFlightDate()
    {
        return _FlightDate;
    }
    public void SetTime(int value)
    {
        _Time = value;

    }
    public int GetTime()
    {
        return _Time;
    }
}
